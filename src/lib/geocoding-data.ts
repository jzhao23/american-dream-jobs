/**
 * Build search index from MSA metadata.
 * Creates word-based index for fuzzy matching on city names.
 */
function buildSearchIndex(): { [term: string]: string[] } {
  const index: { [term: string]: string[] } = {};

  for (const [code, msa] of Object.entries(msaMetadata)) {
    // Add words from short name
    const shortWords = msa.shortName.toLowerCase().split(/\s+/);
    for (const word of shortWords) {
      if (word.length >= 2) {
        if (!index[word]) index[word] = [];
        if (!index[word].includes(code)) index[word].push(code);
      }
    }

    // Add words from full name (excluding state codes)
    const fullName = msa.name.replace(/,\s*[A-Z-]+$/, '');
    const fullWords = fullName.toLowerCase().replace(/[^a-z\s]/g, '').split(/\s+/);
    for (const word of fullWords) {
      if (word.length >= 2) {
        if (!index[word]) index[word] = [];
        if (!index[word].includes(code)) index[word].push(code);
      }
    }
  }

  return index;
}

